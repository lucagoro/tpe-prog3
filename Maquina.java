public class Maquina {
     String nombre;
     int piezas;

    public Maquina(String nombre, int piezas) {
        this.nombre = nombre;
        this.piezas = piezas;
    }


    public String getNombre() {
        return nombre;
    } 
    
    public int getPieza() {
        return piezas;
    }
}
